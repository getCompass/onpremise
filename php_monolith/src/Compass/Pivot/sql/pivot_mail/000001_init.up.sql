CREATE TABLE IF NOT EXISTS `pivot_mail`.`mail_uniq_list_0` (
	`mail_hash` VARCHAR(40) NOT NULL DEFAULT '' COMMENT 'sha-1 хеш адреса почты',
	`user_id` BIGINT(20) NOT NULL DEFAULT 0 COMMENT 'id пользователя, кому принадлежит адрес почты',
	`created_at` INT(11) NOT NULL DEFAULT 0 COMMENT 'когда запись создана',
	`updated_at` INT(11) NOT NULL DEFAULT 0 COMMENT 'когда запись обновлена',
	`password_hash` VARCHAR(60) NOT NULL DEFAULT 0 COMMENT 'sha-1 хеш пароль к аутентификации пользователя через почту',
	PRIMARY KEY (`mail_hash`))
	ENGINE = InnoDB
	DEFAULT CHARACTER SET = utf8
	COMMENT 'Таблица, содержащая хеши адресов почты пользователей';

